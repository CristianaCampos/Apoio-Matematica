function numeral(n)
{
	if (n==1)
		result = "primeira";
	else if (n==2)
		result = "segunda";
	else if (n==3)
		result = "terceira";
	else if (n==4)
		result = "quarta";
	else if (n==5)
		result = "quinta";
	else if (n==6)
		result = "sexta";
	else if (n==7)
		result = "sétima";
	else if (n==8)
		result = "oitava";
	else if (n==9)
		result = "nona";
	else if (n==10)
		result = "décima";
	return result;

}